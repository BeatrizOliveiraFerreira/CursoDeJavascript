let techs = ["html", "css", "js"]

// adiciona um item no fim
console.log(techs.push("nodejs"))

// adiciona no começo
console.log(techs.unshift("sql"))

// remove do fim
console.log(techs.pop())

// remove do começo
console.log(techs.shift())

// pegar elementos do array
console.log(techs.slice(1,3))

// remover itens do array
console.log(techs.splice(1,2))

// encontrar a posição de um elemento no array
console.log(techs.indexOf("css"))


